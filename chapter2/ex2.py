n[9] = [6, (-50, 0), (-50, -50), (0, -50), (0, 0), (0,-50), (-50, -100)]
def number (l, k):
    t.pendown()
    for i in range (1, n[l][0]+1):
        t.goto(n[l][i][0]+k*75, n[l][i][1])
    t.penup()
    t.goto(0 + (k+1)*75, 0)
def index (I):
    i = 0
    while i < 6:
        number (I[i], i)
        i += 1
index(I)