I = [1, 4, 1, 7, 0, 0]
n = [0]*10
n[0] = [4, (-50, 0), (-50, -100), (0, -100), (0, 0)]
n[1] = [4, (-50, -50), (0, 0), (0, -100), (0, 0)]
n[2] = [5, (-50, 0), (0, 0), (0, -50), (-50, -100), (0, -100)]
